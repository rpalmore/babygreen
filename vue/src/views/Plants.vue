import AddPlant from "../components/AddPlant.vue";
import plantService from "../services/PlantService";
import treatmentService from "../services/TreatmentService";
export default {
  components: { AddPlant },
  name: "plants",
  data() {
    return {
      plant: {
        userId: this.$store.state.user.id,
      },
      fields: [
        {
          key: "selectAll",
          label: "",
          // need to add a checkbox here somehow
        },
        {
          key: "plantName",
          sortable: true,
        },
        {
          key: "indoor",
          label: "Location",
          sortable: true,
        },
        {
          key: "careDate",
          label: "Last Watered",
          sortable: true,
        },
        {
          key: "plantImg",
          label: "Image",
        },
      ],
      newPlant: {},
      showForm: false,
      selectedPlantIds: [],
      checkAll: false,
      treatment: {
        plantId: [],
      },
      modal: "",
    };
  },
  computed: {
    plants() {
      return this.$store.state.plants;
    },
    latestWatering() {
      return this.$store.state.latestWatering;
    },
    btnDisabled() {
      return this.selectedPlantIds.length === 0 ? true : false;
    },
  },
  methods: {
    checkSelectedIds() {
      this.selectedPlantIds.length === this.$store.state.plants.length
        ? (this.checkAll = true)
        : (this.checkAll = false);
    },
    logCare() {
      this.treatment.plantId = this.selectedPlantIds;
      treatmentService
        .createTreatment(this.treatment)
        .then((response) => {
          if (response.status == 201) {
            this.$store.commit("ADD_TREATMENT", response.data);
          }
          this.selectedPlantIds = [];
          this.checkAll = false;
          this.treatment = {};
        })
        .catch((err) => {
          alert(err + " problem creating treatment!");
        });
    },
    toggleForm(plant) {
      this.newPlant = plant;
      this.showForm === true ? (this.showForm = false) : (this.showForm = true);
    },
    selectAll() {
      if (this.checkAll === true) {
        for (let i = 0; i < this.plants.length; i++) {
          if (!this.selectedPlantIds.includes(this.plants[i].plantId)) {
            this.selectedPlantIds.push(this.plants[i].plantId);
          }
        }
        // eslint-disable-next-line no-console
        console.log(this.selectedPlantIds);
      } else {
        this.selectedPlantIds = [];
        this.checkAll = false;
      }
    },
    deletePlant(plantId) {
      this.modal = "";
      this.$bvModal
        .msgBoxConfirm("Are you sure you want to delete this plant?")
        .then((value) => {
          this.modal = value;
          if (value === true) {
            plantService
              .deletePlant(plantId)
              .then((response) => {
                if (response.status == 204) {
                  this.$store.commit("DELETE_PLANT", plantId);
                }
              })
              .catch((err) => {
                alert(err + " problem deleting plant!");
              });
          }
        });
    },
    editPlant(newPlant) {
      if (newPlant.plantAge === null) {
        let today = new Date();
        const dd = String(today.getDate()).padStart(2, "0");
        const mm = String(today.getMonth() + 1).padStart(2, "0");
        const yyyy = today.getFullYear();
        today = yyyy + "-" + mm + "-" + dd;
        newPlant.plantAge = today;
      }
      plantService
        .editPlant(newPlant)
        .then((response) => {
          if (response.status == 200) {
            this.$store.commit("EDIT_PLANT", newPlant);
            this.showForm = false;
          }
        })
        .catch((err) => {
          alert(err + " problem editing plant!");
        });
    },
    formatDateDay(date) {
      const options = {
        weekday: "long",
        //year: "numeric",
        month: "long",
        day: "numeric",
      };
      return new Date(date).toLocaleDateString("en-US", options);
    },
  },
  created() {
    treatmentService
      .getLatestWaterings()
      .then((response) => {
        if (response.status == 200) {
          this.$store.commit("SET_LATEST_WATERING", response.data);
        }
      })
      .catch((err) => {
        alert(err + " problem getting latest waterings!");
      });
    plantService.getAllPlants().then((response) => {
      if (response.status == 200) {
        this.$store.commit("SET_PLANTS", response.data);
      }
    });
  },
};
</script>

